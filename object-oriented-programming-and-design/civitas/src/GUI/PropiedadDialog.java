    public PropiedadDialog(java.awt.Frame parent, Jugador jugador) {
        super(parent, true);
        propiedadElegida = -1;
        setLocationRelativeTo(null);
        initComponents();
        
        DefaultListModel propiedades = new DefaultListModel<>(); // datos para la lista
        ArrayList<String> opciones = new ArrayList<> ();
        
        for (int i=0; i < jugador.getNumPropiedades(); i++){
            opciones.add(jugador.getPropiedades().get(i).getNombre());
        }
        
        for (String s: opciones){
            propiedades.addElement(s);
        } //se completan los datos
        listaPropiedades.setModel(propiedades); //se le dice a la lista cuÃ¡les son esos datos
        
        setVisible(true);
        repaint();
        revalidate();
    }
    
    public int getPropiedadElegida (){
        return propiedadElegida;
    }

