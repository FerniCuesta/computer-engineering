    public void pausa(){
        int val= JOptionPane.showConfirmDialog(null, "¿Continuar?",
        "Siguiente paso", JOptionPane.YES_NO_OPTION);
        if (val==1) System.exit(0);
    }
       
    public Respuesta comprar(){
        int val= JOptionPane.showConfirmDialog(null, "¿Comprar la calle actual?",
        "Compra", JOptionPane.YES_NO_OPTION);
        if (val==0){
            return Respuesta.SI;
        }else{
            return Respuesta.NO;
        }
    }

    public OperacionInmobiliaria elegirOperacion(){
        
        GestionarDialog gestion;
       
        gestion = new GestionarDialog(this);
        
        switch (gestion.getGestion()) {
            case 0:
                return OperacionInmobiliaria.CONSTRUIR_CASA;
            case 1:
                return OperacionInmobiliaria.CONSTRUIR_HOTEL;
            default:
                return OperacionInmobiliaria.TERMINAR;
        }   
        
    }

    public int elegirPropiedad(){
           
        PropiedadDialog propiedad;
       
        propiedad = new PropiedadDialog(this, juego.getJugadorActual());
        
        return propiedad.getPropiedadElegida();
        
    }

    public void mostrarSiguienteOperacion(OperacionJuego operacion) {
        
        text_operacion.setText(operacion.toString());
        
        if (operacion == OperacionJuego.AVANZAR){
            Dado.createInstance(this);
        }
                
        System.out.println(operacion.toString());
        
        
        repaint();
        revalidate();
        
    }
    
    public void mostrarEventos() {
        
        DiarioDialog diarioD;
        
        if (!Diario.getInstance().getEventos().isEmpty())
            diarioD = new DiarioDialog(this); //crea la ventana del diario
    }

