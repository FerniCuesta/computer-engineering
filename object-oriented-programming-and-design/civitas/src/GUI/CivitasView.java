    @Override
    public void actualiza (){
        
        jugadorPanel1.setJugador(juego.getJugadorActual());
        casillaActualPanel1.setCasilla(juego.getTablero().getCasilla(juego.getJugadorActual().getCasillaActual()));
        
        if (juego.finalDelJuego()){
            label_ranking.setVisible(true);
            text_ranking.setVisible(true);
            
            juego.ranking();
            
            String rank = "";

            for (int i=0; i < juego.getJugadores().size(); i++){
                
                rank += "Puesto número " + String.valueOf(i+1) + ": \n" + juego.getJugadores().get(i).toString() + "\n";
                
            }
            
            text_ranking.setText(rank);
            
            repaint();
            revalidate();
        }
        
    }
    
